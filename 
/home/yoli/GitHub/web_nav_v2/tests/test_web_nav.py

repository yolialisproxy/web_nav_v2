import json
import os
import sys
import unittest
from pathlib import Path

# Add project root to path
PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

# Test data paths
DATA_PATH = PROJECT_ROOT / "data" / "websites.json"

def load_sites():
    """Load websites data"""
    with open(DATA_PATH, 'r', encoding='utf-8') as f:
        return json.load(f)


class TestDataIntegrity(unittest.TestCase):
    """Test data integrity and structure"""

    def test_data_file_exists(self):
        """Verify data file exists"""
        self.assertTrue(DATA_PATH.exists(), "Data file should exist")

    def test_data_is_valid_json(self):
        """Verify data is valid JSON"""
        sites = load_sites()
        self.assertIsInstance(sites, list, "Data should be a list")

    def test_data_not_empty(self):
        """Verify data is not empty"""
        sites = load_sites()
        self.assertGreater(len(sites), 0, "Data should not be empty")

    def test_each_site_has_required_fields(self):
        """Verify each site has required fields"""
        sites = load_sites()
        required_fields = ['name', 'url', 'category']
        
        for site in sites:
            for field in required_fields:
                self.assertIn(field, site, f"Site missing required field: {field}")
                self.assertTrue(site[field], f"Field '{field}' should not be empty")

    def test_url_format(self):
        """Verify URL format is valid"""
        sites = load_sites()
        for site in sites:
            url = site['url']
            self.assertTrue(
                url.startswith('http://') or url.startswith('https://'),
                f"URL should start with http:// or https://: {url}"
            )

    def test_category_count(self):
        """Verify reasonable number of categories"""
        sites = load_sites()
        categories = set(site['category'] for site in sites)
        self.assertGreater(len(categories), 10, "Should have more than 10 categories")
        self.assertLess(len(categories), 300, "Should have less than 300 categories")


class TestCategoryBalance(unittest.TestCase):
    """Test category distribution"""

    def test_category_distribution(self):
        """Verify category distribution is reasonable"""
        sites = load_sites()
        category_counts = {}
        
        for site in sites:
            cat = site['category']
            category_counts[cat] = category_counts.get(cat, 0) + 1
        
        # Check that most categories have sites
        self.assertGreater(len(category_counts), 50, "Should have diverse categories")
        
        # Check for outliers (categories with very few or very many sites)
        valid_categories = [count for count in category_counts.values() if 5 <= count <= 1000]
        self.assertGreater(len(valid_categories), len(category_counts) * 0.8, 
                          "Most categories should have reasonable site counts")


class TestPerformance(unittest.TestCase):
    """Test performance requirements"""

    def test_json_load_time(self):
        """Verify JSON loads within acceptable time"""
        import time
        
        start = time.time()
        sites = load_sites()
        load_time = time.time() - start
        
        # Should load within 2 seconds
        self.assertLess(load_time, 2.0, f"JSON load time too high: {load_time:.2f}s")

    def test_data_size(self):
        """Verify data size is reasonable"""
        size = DATA_PATH.stat().st_size
        # Should be less than 10MB
        self.assertLess(size, 10 * 1024 * 1024, f"Data size too large: {size / 1024 / 1024:.2f}MB")


